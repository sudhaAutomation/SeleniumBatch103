package oops;

public class Encapsulation {

	private String username;
	private String password;

	public void setusername(String uname) {
		username = uname;
	}

	public String getusername() {
		return username;
	}

	public void setpassword(String pswrd) {
		password = pswrd;
	}

	public String getpassword() {
		return password;
	}

}
